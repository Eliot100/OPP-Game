package dataStructure;

public class microEdge {
	private int src;
	private double w;
	private int dest;
	public int getSrc() {
		return src;
	}
	public void setSrc(int src) {
		this.src = src;
	}
	public int getDest() {
		return dest;
	}
	public void setDest(int dest) {
		this.dest = dest;
	}
	public double getW() {
		return w;
	}
	public void setW(double w) {
		this.w = w;
	}
}